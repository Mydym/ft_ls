#include "ft_ls.h"

int		ft_lstishidden(char *name)
{
	if (name && (name[0] == '.' && ((name[1] == '\0') ||
					(name[1] == '.' && name[2] == '\0') ||
					ft_isalpha(name[1]))))
		return (1);
	return (0);
}
