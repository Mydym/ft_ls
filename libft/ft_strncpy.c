#include "libft.h"

char	*ft_strncpy(char *dest, const char *src, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		if (i > ft_strlen(src))
			dest[i] = '\0';
		else
			dest[i] = (char)src[i];
		i++;
	}
	return (dest);
}
