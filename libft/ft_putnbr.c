#include "libft.h"

void	ft_putnbr(long n)
{
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else if (n < 0)
	{
		ft_putchar('-');
		if (n < -9)
			ft_putnbr((0 - n) / 10);
		ft_putchar((n % 10) * -1 + '0');
	}
	else
		ft_putchar(n + '0');
}
